//Helper.c

void DisplayTable(int iNo)
{
    if (iNo == 0)
    {
        return;
    }
    
    if (iNo < 0)
    {
        iNo = -iNo;
    }
    
    for (register int  i = 1; i <= 10; i++)
    {
        printf("%d\n",i*iNo);
    }
}