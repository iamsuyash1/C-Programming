//Helper.c

void Display(int iNo1, int iNo2)
{
    int iCnt = 0;

    for (iCnt = 1; iCnt <= iNo1; iCnt++ )
    {
        for (int iCnt2 = 1; iCnt2 <= iNo2; iCnt2++)
        {
            printf("*\t");
        }
        printf("\n");
    }


}