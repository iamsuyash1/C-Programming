#include<stdio.h>

void Display(int);

//========================================================================================================================================================================
//Main.c

int main ()
{
    int iValue = 0;

    printf("Enter the number\n");
    scanf("%d",&iValue);
    Display(iValue);  

    return 0;
}
//========================================================================================================================================================================
//Helper.c

void Display(int no)
{
    int iCnt = 0;
    for(iCnt = no; iCnt >= 1; iCnt--)
    {
        printf("%d\t",iCnt);
    }
}

